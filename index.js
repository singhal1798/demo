// This is a JavaScript demo file

// Variables
var name = "John";
var age = 25;

// Output
console.log("Hello, " + name + "!");
console.log("You are " + age + " years old.");

// Conditional statement
if (age >= 18) {
    console.log("You are an adult.");
} else {
    console.log("You are a minor.");
}

// Function
function greet(name) {
    console.log("Hello, " + name + "!");
}

// Function call
greet("Alice");
